result = [] #list of tuples
classes = set(g.subjects(RDF.type, RDFS.Class))
for c in classes:
    supers = list(g.objects(c, RDFS.subClassOf))
    if supers:
        for s in supers:
            result.append((c, s))
    else:
        result.append((c, None))
